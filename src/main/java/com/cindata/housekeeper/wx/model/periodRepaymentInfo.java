package com.cindata.housekeeper.wx.model;

import java.math.BigDecimal;
import java.sql.Date;

public class periodRepaymentInfo {

	private Date RepaymentDate;
	private String RepaymentChannel;//还款渠道
	private String RepaymentType;//还款类型
	private BigDecimal RepaymentPrice;//还款金额
	private Integer RepaymentDateAlready;//已还款期限
	private Integer RepaymentDateTotal;//还款总期限
	private String RepaymentSchedule;//还款进度
	private String rentStatus;
	
	
	
	public String getRentStatus() {
		return rentStatus;
	}
	public void setRentStatus(String rentStatus) {
		this.rentStatus = rentStatus;
	}
	public Date getRepaymentDate() {
		return RepaymentDate;
	}
	public void setRepaymentDate(Date repaymentDate) {
		RepaymentDate = repaymentDate;
	}
	public String getRepaymentChannel() {
		return RepaymentChannel;
	}
	public void setRepaymentChannel(String repaymentChannel) {
		RepaymentChannel = repaymentChannel;
	}
	public String getRepaymentType() {
		return RepaymentType;
	}
	public void setRepaymentType(String repaymentType) {
		RepaymentType = repaymentType;
	}
	public BigDecimal getRepaymentPrice() {
		return RepaymentPrice;
	}
	public void setRepaymentPrice(BigDecimal repaymentPrice) {
		RepaymentPrice = repaymentPrice;
	}
	public Integer getRepaymentDateAlready() {
		return RepaymentDateAlready;
	}
	public void setRepaymentDateAlready(Integer repaymentDateAlready) {
		RepaymentDateAlready = repaymentDateAlready;
	}
	public Integer getRepaymentDateTotal() {
		return RepaymentDateTotal;
	}
	public void setRepaymentDateTotal(Integer repaymentDateTotal) {
		RepaymentDateTotal = repaymentDateTotal;
	}
	public String getRepaymentSchedule() {
		return RepaymentSchedule;
	}
	public void setRepaymentSchedule(String repaymentSchedule) {
		RepaymentSchedule = repaymentSchedule;
	}
	@Override
	public String toString() {
		return "periodRepaymentInfo [RepaymentDate=" + RepaymentDate + ", RepaymentChannel=" + RepaymentChannel
				+ ", RepaymentType=" + RepaymentType + ", RepaymentPrice=" + RepaymentPrice + ", RepaymentDateAlready="
				+ RepaymentDateAlready + ", RepaymentDateTotal=" + RepaymentDateTotal + ", RepaymentSchedule="
				+ RepaymentSchedule + ", rentStatus=" + rentStatus + "]";
	}
	
	
	
}
